#include "3-calc.h"
/**
 * op_add - add intergers
 * @a: first int
 * @b: second int
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - sub intergers
 * @a: first int
 * @b: second int
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - mul intergers
 * @a: first int
 * @b: second int
 * Return: mul
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - div intergers
 * @a: first int
 * @b: second int
 * Return: div
 */

int op_div(int a, int b)
{
return (a / b);
}


/**
 * op_mod - mod intergers
 * @a: first int
 * @b: second int
 * Return: mod
 */
int op_mod(int a, int b)
{
return (a % b);
}
